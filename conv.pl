);

sub polish_text {
    my $text = shift;
    # Исправлє́нїе начальнагѡ ѹ҆ка (Ჹ҆ -> ѹ҆)
    $text =~ s/Ჹ҆/ѹ҆/g;
    # Ѻ҆ме́га въ нача́лѣ сло́ва съ придыха́нїемъ
    $text =~ s/\bѡ/ѻ҆/g;
    # Ѹ҆́къ въ нача́лѣ сло́ва
    $text =~ s/\bꙋ/ѹ/g;
    # Бг҃ъ въ падежахъ
    $text =~ s/бGа/бг҃а/g;
    $text =~ s/бGу/бг҃ꙋ/g;
    $text =~ s/бGе/бж҃е/g;
    # Гдⷭ҇ь
    $text =~ s/гDи/гдⷭ҇и/g;
    # Специфические исправлє́нїя (тьмꙋ -> тмꙋ)
    $text =~ s/тьмꙋ̀/тмꙋ̀/g;
    # Мѣстоиме́нїѧ
    $text =~ s/(\p{L}+́)\s+мѧ/$1 мѧ/g;
    $text =~ s/(\p{L}+́)\s+ми/$1 ми/g;
    return $text;
}

my $word_pattern = join '|', sort { length($b) <=> length($a) } map { quotemeta } keys %word_replacements;
my $char_pattern = join '|', sort { length($b) <=> length($a) } map { quotemeta } keys %char_replacements;

while (my $line = <STDIN>) {
    $line =~ s/($word_pattern)/$word_replacements{$1}/g if $word_pattern;
    $line =~ s/($char_pattern)/$char_replacements{$1}/g if $char_pattern;
    $line = polish_text($line);
    print $line;
}
